const String coinAPIURL = 'https://rest.coinapi.io/v1/exchangerate';
const String apiKey = 'YOUR-API-Key';

class CoinData {
  Future getCoinLastPrice(String selectedCurrency, String coin) async {
    String requestURL = '$coinAPIURL/$coin/$selectedCurrency?apikey=$apiKey';

    http.Response response = await http.get(Uri.parse(requestURL));

    if (response.statusCode == 200) {
      var decodedData = jsonDecode(response.body);
      var lastPrice = decodedData['rate'];
      return lastPrice;
    } else {
      print(response.statusCode);
      throw Exception('Problem with the get request');
    }
  }
}
